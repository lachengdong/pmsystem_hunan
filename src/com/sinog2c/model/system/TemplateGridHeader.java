package com.sinog2c.model.system;

public class TemplateGridHeader {
	private String field;
	private String description;
	private String width;
	private String headerAlign;
	private String align;
	private String allowSort;

	public String getField() {
		return field;
	}

	public void setField(String field) {
		this.field = field;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getWidth() {
		return width;
	}

	public void setWidth(String width) {
		this.width = width;
	}

	public String getHeaderAlign() {
		return headerAlign;
	}

	public void setHeaderAlign(String headerAlign) {
		this.headerAlign = headerAlign;
	}

	public String getAlign() {
		return align;
	}

	public void setAlign(String align) {
		this.align = align;
	}

	public String getAllowSort() {
		return allowSort;
	}

	public void setAllowSort(String allowSort) {
		this.allowSort = allowSort;
	}

}
